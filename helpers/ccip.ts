const getAttestation = async (
  senderAddress: string,
  sourceChain: string,
  destinationChain: string,
  amount: number,
  message: string
) => {
  const apiResponse = await fetch(
    "https://nfc-encryption.vercel.app/attestation",
    {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        senderAddress,
        sourceChain,
        destinationChain,
        amount,
        message,
      }),
    }
  );

  const { encrypted } = await apiResponse.json();

  return encrypted;
};

